import SwiftUI
import Foundation

extension Color {
    static let theme = Theme()
}

struct Theme {
    let primary = Color(hex: "7f13ec")
    let primaryDark = Color(hex: "600eb3")
    let backgroundLight = Color(hex: "f7f6f8")
    let backgroundDark = Color(hex: "191022")
    let cardDark = Color(hex: "2a2235")
    let surfaceDark = Color(hex: "251b30")
    let textSecondary = Color(hex: "ab9db9")
}

extension Color {
    init(hex: String) {
        let hex = hex.trimmingCharacters(in: CharacterSet.alphanumerics.inverted)
        var int: UInt64 = 0
        Scanner(string: hex).scanHexInt64(&int)
        let a, r, g, b: UInt64
        switch hex.count {
        case 3: // RGB (12-bit)
            (a, r, g, b) = (255, (int >> 8) * 17, (int >> 4 & 0xF) * 17, (int & 0xF) * 17)
        case 6: // RGB (24-bit)
            (a, r, g, b) = (255, int >> 16, int >> 8 & 0xFF, int & 0xFF)
        case 8: // ARGB (32-bit)
            (a, r, g, b) = (int >> 24, int >> 16 & 0xFF, int >> 8 & 0xFF, int & 0xFF)
        default:
            (a, r, g, b) = (1, 1, 1, 0)
        }

        self.init(
            .sRGB,
            red: Double(r) / 255,
            green: Double(g) / 255,
            blue:  Double(b) / 255,
            opacity: Double(a) / 255
        )
    }
}
